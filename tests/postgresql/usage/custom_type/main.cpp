#include <cassert>
#include <iostream>

#include "tab_point.h"
#include <sqlpp23/tests/postgresql/all.h>


namespace sql = sqlpp::postgresql;

int main(int, char*[]) {
  try {
    auto db = sql::make_test_connection();

    test::createTabPoint(db);
    const auto t = test::TabPoint{};

    // --- INSERT
    // --------------------------------------------------------------- id is
    // generated by PostgreSQL; only color is required.
    db(insert_into(t).set(t.color = Color::red));
    db(insert_into(t).set(t.color = Color::green));
    db(insert_into(t).set(t.color = Color::blue));

    // --- SELECT
    // --------------------------------------------------------------- row.id is
    // PointId, row.color is Color — no unwrapping at the call site.
    {
      int count = 0;
      for (const auto& row :
           db(select(all_of(t)).from(t).order_by(t.id.asc()))) {
        static_assert(std::is_same_v<decltype(row.id), PointId>);
        static_assert(std::is_same_v<decltype(row.color), Color>);
        ++count;
        assert(row.id == PointId{count});
      }
      assert(count == 3);
    }

    // --- UPDATE
    // --------------------------------------------------------------- Filter by
    // PointId, set a new Color.
    db(update(t).set(t.color = Color::blue).where(t.id == PointId{1}));

    {
      for (const auto& row :
           db(select(t.color).from(t).where(t.id == PointId{1}))) {
        assert(row.color == Color::blue);
      }
    }

    // --- DELETE
    // --------------------------------------------------------------- Filter by
    // Color — removes all blue rows (id 1 and 3).
    db(delete_from(t).where(t.color == Color::blue));

    {
      int count = 0;
      for (const auto& row : db(select(all_of(t)).from(t))) {
        assert(row.color == Color::green);
        ++count;
      }
      assert(count == 1);
    }

    // --- PREPARED INSERT
    // ------------------------------------------------------
    {
      auto stmt =
          db.prepare(insert_into(t).set(t.color = sqlpp::parameter(t.color)));
      stmt.parameters.color = Color::red;
      db(stmt);
      stmt.parameters.color = Color::blue;
      db(stmt);
    }

    // --- PREPARED SELECT
    // ------------------------------------------------------ Filter by Color;
    // verify both PointId and Color come back as the right types.
    {
      auto stmt = db.prepare(select(all_of(t)).from(t).where(
          t.color == sqlpp::parameter(t.color)));
      stmt.parameters.color = Color::green;
      for (const auto& row : db(stmt)) {
        static_assert(std::is_same_v<decltype(row.id), PointId>);
        static_assert(std::is_same_v<decltype(row.color), Color>);
        assert(row.color == Color::green);
      }
    }

    // --- PREPARED UPDATE
    // ------------------------------------------------------ Filter by PointId,
    // set a new Color.
    {
      auto stmt = db.prepare(update(t)
                                 .set(t.color = sqlpp::parameter(t.color))
                                 .where(t.id == sqlpp::parameter(t.id)));
      stmt.parameters.color = Color::blue;
      stmt.parameters.id = PointId{2};
      db(stmt);
    }

    // --- PREPARED DELETE
    // ------------------------------------------------------ Filter by PointId.
    {
      auto stmt =
          db.prepare(delete_from(t).where(t.id == sqlpp::parameter(t.id)));
      stmt.parameters.id = PointId{2};
      db(stmt);
    }

  } catch (const std::exception& e) {
    std::cerr << "Exception: " << e.what() << std::endl;
    return 1;
  }
  return 0;
}
